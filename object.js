//objects & reference types

// in reference types we will have 
// Object
// Array
// function


let person = {
    name : 'sruti',
    age : 24
};

console.log(person);

//fyi {} this is called object literal
// typeof person
// 'object'

//dot notation to initialies variable inside object :

person.name = 'anandita';
console.log(person.name);

//typeof person.name
// 'string'
// typeof age
// 'number'

//bracket notation :

person['name'] = 'elyy'
console.log(person.name);

