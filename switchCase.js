let role = 'admin';

switch(role){
    case 'guest' :
        console.log('Guest User');
        break;
    case 'moderator' :
        console.log('moderator user');
        break;
    case 'admin' :
        console.log('admin user');
        break;    
    default :
        console.log('unknown user');
}

//break statement to end the case 

